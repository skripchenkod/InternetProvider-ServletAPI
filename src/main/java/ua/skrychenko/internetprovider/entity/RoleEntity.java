package ua.skrychenko.internetprovider.entity;

public class RoleEntity {
    private int id;
    private String name;

    public RoleEntity(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
